package com.company;

public class Main {

    public static void main(String[] args) {
	// write your code here
        int i, j;
        System.out.println( "-----------------------------------------------------------------------------------------------------------------------------------------------------------------");
        for ( j = 0; j++ < 10; ) {
            System.out.printf( "| " );
            for  ( i = 0; i++ < 10; )
                System.out.printf( "%2d * %2d = %3d | ", j, i, i * j );
            System.out.println();
        }

        System.out.println( "-----------------------------------------------------------------------------------------------------------------------------------------------------------------");

        i = 1;
        j = 1;

        while ( j < 11 ) {
            System.out.printf( "| " );
            while (i < 11) {
                System.out.printf( "%2d * %2d = %3d | ", j, i, i * j );
                i++;
            }
            System.out.println();
            j++;
            i = 1;
        }

        System.out.println( "-----------------------------------------------------------------------------------------------------------------------------------------------------------------");

        i = 1;
        j = 1;

        do {
            System.out.printf( "| " );
            do {
                System.out.printf( "%2d * %2d = %3d | ", j, i, i * j );
            } while ( i++ < 10 );
            System.out.println();
            i = 1;
        } while( j++ < 10 );
        System.out.println( "-----------------------------------------------------------------------------------------------------------------------------------------------------------------");
    }
}
